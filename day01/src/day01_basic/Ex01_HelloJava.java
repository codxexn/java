package day01_basic;	// 패키지 명시

public class Ex01_HelloJava {	// 클래스명 : 소스파일명과 같다
	public static void main(String[] args) {	// main 함수 (x) -> main method (o)
		
		// 주석 : C와 같은 방법으로 처리 ( //, /* */ )
		// 이클립스 주석 자동 완성 : ctrl + /
		
		// 실행 ctrl + F11
		
		System.out.println("Hello Java world"); // ln : line -> 줄바꿈
		System.out.print("Hello Java world");
		
		// 자동완성 기능 :  ctrl + space
		// system.out.println( ); -> sysn + ctrl + space
		System.out.println("Hello world");
		
		int b = 100000;
		System.out.println(b);
		
		
	}

}
